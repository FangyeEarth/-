package cn.wuye.service.ex;

public class SecretAnswerNotMatchException extends ServiceException {

	private static final long serialVersionUID = 5745911538333911126L;

	public SecretAnswerNotMatchException() {
		super();
	}

	public SecretAnswerNotMatchException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SecretAnswerNotMatchException(String message, Throwable cause) {
		super(message, cause);
	}

	public SecretAnswerNotMatchException(String message) {
		super(message);
	}

	public SecretAnswerNotMatchException(Throwable cause) {
		super(cause);
	}
	
	
	
	
	

}
